void sendCommand(uint8_t command_s)
{
	temp[0] = COMAND;
	temp[1] = command_s;
	HAL_I2C_Master_Transmit(&hi2c1, OLED_adress, temp, 2, 100);
}

void sendData(uint8_t data_s)
{
	temp[0] = DATA;
	temp[1] = data_s;
	HAL_I2C_Master_Transmit(&hi2c1, OLED_adress, temp, 2, 100);
}

void LCD_GoTo(unsigned char x, unsigned char y)
{
	LCD_X = x;
	LCD_Y = y;
	sendCommand(0xB0 + y);
	sendCommand(x & 0xf);
	sendCommand(0x10 | (x >> 4));
}


void LCD_Char2(unsigned int c)
{
	unsigned char x = 0;
	temp_char[0] = 0x40;
	for (x = 0; x < 5; x++)
	{
		temp_char[x + 1] = sym[c][x];
	}
	temp_char[6] = 0;
	HAL_I2C_Master_Transmit(&hi2c1, OLED_adress, temp_char, 7, 1000);

	LCD_X += 8;
	if (LCD_X > OLED_WIDTH)
	{
		LCD_X = OLED_DEFAULT_SPACE;
		LCD_Y += 1;
	}
}

void HexToChar(unsigned int H)
{
	
	 
	//HexToBitIn32_Char = "0000000000";
	uint8_t i = 0;
	for (i = 0; i < 32; i++)HexToBitIn32_Char[i] = 0;
	i = 0;
	do
	{
		HexToBitIn32_Char[i++] =  (H % 2);
		H = H / 2;
	} while (H > 0);
	i = 33;
}

