void LCD_Clear(void)
{
	unsigned short i;
	unsigned short x = 0;
	unsigned short y = 0;
	LCD_GoTo(0, 0);

	for (i = 0; i < (OLED_BUFFERSIZE); i++) //(SSD1306_LCDWIDTH*SSD1306_LCDHEIGHT/8)
		{
			LCD_Char2(' ');
			x++;
			if (x > OLED_WIDTH)
			{
				x = 0;
				y++;
				LCD_GoTo(0, y);
			}
		}
	LCD_X = OLED_DEFAULT_SPACE;
	LCD_Y = 0;
}


